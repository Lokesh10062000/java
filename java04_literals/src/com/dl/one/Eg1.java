package com.dl.one;

public class Eg1 {
	
	public static void main(String[]args) {
		
		
		//Integer literals
		int i=10;
		System.out.println(i);
		//floting point literals
		float f=12.3f;
		System.out.println(f);
		//string literals
		String st="lokesh";
		System.out.println(st);
		//Boolean literals
		boolean b=true;
		System.out.println(b);
		
		//character literals
		char ch='a';
		System.out.println(ch);
	}

}
